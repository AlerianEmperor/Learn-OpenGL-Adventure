	glDepthFunc(GL_LESS);
	Shader shader("Depth_Test.vs", "Depth_Test.fs");

	Model model("E:/Models/crytek_sponza/crytek_sponza.obj");

	// set up vertex data (and buffer(s)) and configure vertex attributes
	// ------------------------------------------------------------------
